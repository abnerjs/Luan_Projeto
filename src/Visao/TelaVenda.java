        int position = tabVenda.getSelectedRow();

        if (position == -1) {
            JOptionPane.showMessageDialog(this, "Selecione a venda na tabela.", "Erro em alterar!", 1);
        } else {
            vendaSelecionado = vendaList.get(position);

            panGuia.setEnabledAt(1, true);
            panGuia.setSelectedIndex(1);
            panGuia.setEnabledAt(0, false);

            txtValor.setValue(vendaSelecionado.getValor());
            txtDesconto.setValue(vendaSelecionado.getDesconto());
            listItemVenda.clear();
            /*           for (ItemVenda iv : vendaSelecionado.getItemVendaList()) {
                listItemVenda.add(iv.getCodProduto());
            }
             */
            alterarC = vendaSelecionado.getCodVenda();
            this.novo = false;
        }
        //na hroa de pegar o ccliente de celular colocar celularselecionado celular.getCliente.getnome
