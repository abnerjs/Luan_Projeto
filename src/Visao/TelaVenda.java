    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed
        Venda venda = new Venda();

        venda.setDesconto(0.0);
        venda.setValor(0.0);
        venda.setDataVenda(Data.getDate());

        for (ItemVenda p : listItemVenda) {
            p.setVenda(venda);
        }

        venda.setItemVendaList(listItemVenda);

        if (this.novo) {
            controle.create(venda);
        } else {
            venda.setCodVenda(alterarC);
            try {
                controle.edit(venda);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "erro na exclusao");
            }
        }

        panGuia.setEnabledAt(0, true);
        panGuia.setSelectedIndex(0);
        panGuia.setEnabledAt(1, false);
        vendaList.clear();
        vendaList.addAll(controle.findVendaEntities());
//dar um return para atualizar a lista 

    }//GEN-LAST:event_SalvarActionPerformed

