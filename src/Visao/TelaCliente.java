    private void botSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botSalvarActionPerformed
        Cliente cliente = new Cliente();

        cliente.setNome(txtNome.getText());
        cliente.setEndereco(txtEndereco.getText());
        cliente.setTelefone(txtTelefone.getText());   
        cliente.setCpf(txtCPF.getText());
        cliente.setCep(txtCEP.getText());
        cliente.setEstado(txtEstado.getSelectedItem().toString());
        cliente.setCidade(txtCidade.getText());
        
        if (this.novo) {
            controle.create(cliente);
        } else {
            cliente.setCodCliente(alterarC);
            try {
                controle.edit(cliente);
            } catch (Exception ex) {
                Logger.getLogger(TelaCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        panGuia.setEnabledAt(0, true);
        panGuia.setSelectedIndex(0);
        panGuia.setEnabledAt(1, false);
        listaClientes.clear();
        listaClientes.addAll(controle.findClienteEntities());
    }//GEN-LAST:event_botSalvarActionPerformed

