    private void botAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botAlterarActionPerformed
        Cliente clienteSelecionado;

        int position = tabCliente.getSelectedRow();

        if (position == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um cliente na tabela.", "Erro em alterar!", 1);
        } else {
            clienteSelecionado = listaClientes.get(position);
            panGuia.setEnabledAt(1, true);
            panGuia.setSelectedIndex(1);
            panGuia.setEnabledAt(0, false);
            txtEndereco.setText(clienteSelecionado.getEndereco());
            txtNome.setText(clienteSelecionado.getNome());
            txtTelefone.setText(clienteSelecionado.getTelefone());
            txtCPF.setText(clienteSelecionado.getCpf());
            txtCidade.setText(clienteSelecionado.getCidade());
            txtEstado.setSelectedItem(clienteSelecionado.getEstado());
            txtCEP.setText(clienteSelecionado.getCep());
            alterarC = clienteSelecionado.getCodCliente();
            this.novo = false;
        }
    }//GEN-LAST:event_botAlterarActionPerformed

    private void botExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botExcluirActionPerformed
        Cliente clienteExcluir;
        int posicao = tabCliente.getSelectedRow();

        if (posicao == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um cliente na tabela.",
                "Erro na exclusão!", 0);
        } else {
            clienteExcluir = listaClientes.get(posicao);

            int resposta = JOptionPane.showConfirmDialog(this,
                "Deseja exluir o Cliente \"" + clienteExcluir.getNome() + "\"?",
                "Exclusão de cliente", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                try {
                    controle.destroy(clienteExcluir.getCodCliente());
                } catch (NonexistentEntityException ex) {
                    Logger.getLogger(TelaCliente.class.getName()).log(Level.SEVERE, null, ex);
                }
                listaClientes.clear();
                listaClientes.addAll(controle.findClienteEntities());
            }
        }
    }//GEN-LAST:event_botExcluirActionPerformed

