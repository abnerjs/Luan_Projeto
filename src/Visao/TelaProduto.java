    private void botSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botSalvarActionPerformed
        Produto produto = new Produto();

        //getvalue serve poara campos formatados
        produto.setDescricao(txtDescricao.getText());
        produto.setValor(((Number)txtValor.getValue()).doubleValue()); //pegando valor como numero e colocando como double
        produto.setQuant(((Number)txtQuant.getValue()).intValue());

        if (this.novo) {
            controle.create(produto);
        } else {
            produto.setCodProduto(alterarC);
            try {
                controle.edit(produto);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "erro ao Salvar");
            }
        }
        panGuia.setEnabledAt(0, true);
        panGuia.setSelectedIndex(0);
        panGuia.setEnabledAt(1, false);
        listaProdutos.clear();
        listaProdutos.addAll(controle.findProdutoEntities());

    }//GEN-LAST:event_botSalvarActionPerformed


