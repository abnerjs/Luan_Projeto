    private void botAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botAlterarActionPerformed
        Produto produtoSelecionado;

        int position = tabProduto.getSelectedRow();

        if (position == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um produto na tabela.", "Erro em alterar!", 1);
        } else {
            produtoSelecionado = listaProdutos.get(position);
            panGuia.setEnabledAt(1, true);
            panGuia.setSelectedIndex(1);
            panGuia.setEnabledAt(0, false);
            
            txtDescricao.setText(produtoSelecionado.getDescricao());
            txtValor.setValue(produtoSelecionado.getValor());
            txtQuant.setValue(produtoSelecionado.getQuant());
            
            alterarC = produtoSelecionado.getCodProduto();
            this.novo = false;
        }
    }//GEN-LAST:event_botAlterarActionPerformed

    private void botExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botExcluirActionPerformed
        Produto produtoExcluir;
        
        int posicao = tabProduto.getSelectedRow();

        if (posicao == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um produto na tabela.",
                "Erro na exclusão!", 0);
        } else {
            produtoExcluir = listaProdutos.get(posicao);

            int resposta = JOptionPane.showConfirmDialog(this,
                "Deseja exluir o Produto \"" + produtoExcluir.getCodProduto() + "\"?",
                "Exclusão de PRoduto", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                try {
                    controle.destroy(produtoExcluir.getCodProduto());
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "erro na exclusao");
                }
                listaProdutos.clear();
                listaProdutos.addAll(controle.findProdutoEntities());
            }
        }
    }//GEN-LAST:event_botExcluirActionPerformed

