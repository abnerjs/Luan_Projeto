    private void botSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botSalvarActionPerformed
        Celular celular = new Celular();

        celular.setModelo(txtModelo.getText());
        
        celular.setMarca(txtMarca.getText());
        celular.setCor(txtCor.getText());
        celular.setImei(Integer.valueOf(txtImei.getText()));
        celular.setCliente(ClienteSelecionado);
        
        if (this.novo) {
            controle.create(celular);
        } else {
            celular.setCodCelular(alterarC);
            try {
                controle.edit(celular);
            } catch (Exception ex) {
               // Logger.getLogger(TelaCelular.class.get()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null,"ops.. houve um erro verifique os campos!...");
            }
        }
        panGuia.setEnabledAt(0, true);
        panGuia.setSelectedIndex(0);
        panGuia.setEnabledAt(1, false);
        celularList.clear();
        celularList.addAll(controle.findCelularEntities());
    }//GEN-LAST:event_botSalvarActionPerformed

