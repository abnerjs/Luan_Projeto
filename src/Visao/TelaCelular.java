    private void botAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botAlterarActionPerformed
      Celular celularSelecionado;

        int position = tabCelular.getSelectedRow();

        if (position == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um cliente na tabela.", "Erro em alterar!", 1);
        } else {
            celularSelecionado = celularList.get(position);
            panGuia.setEnabledAt(1, true);
            panGuia.setSelectedIndex(1);
            panGuia.setEnabledAt(0, false);
            
            txtMarca.setText(celularSelecionado.getMarca());
            txtCor.setText(celularSelecionado.getCor());
            txtModelo.setText(celularSelecionado.getModelo());
            txtCliente.setText(celularSelecionado.getCliente().getNome());
            
            txtImei.setText(String.valueOf(celularSelecionado.getImei()));
            
            
            alterarC = celularSelecionado.getCodCelular();
            this.novo = false;
        }
        
        //na hroa de pegar o ccliente de celular colocar celularselecionado celular.getCliente.getnome
    }//GEN-LAST:event_botAlterarActionPerformed

    private void botExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botExcluirActionPerformed
      Celular celularExcluir;
        int posicao = tabCelular.getSelectedRow();

        if (posicao == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um celular na tabela.",
                "Erro na exclusão!", 0);
        } else {
            celularExcluir = celularList.get(posicao);

            int resposta = JOptionPane.showConfirmDialog(this,
                "Deseja exluir o Cliente \"" + celularExcluir.getModelo() + "\"?",
                "Exclusão de cliente", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                try {
                    controle.destroy(celularExcluir.getCodCelular());
                } catch (NonexistentEntityException ex) {
                    JOptionPane.showMessageDialog(this, "erro na exclusao");
                }
                celularList.clear();
                celularList.addAll(controle.findCelularEntities());
            }
        }
    }//GEN-LAST:event_botExcluirActionPerformed

